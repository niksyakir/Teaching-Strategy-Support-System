                    <p class="head">DETAILED DESCRIPTION</p>
                    <p class="details"><b>What is Cooperative Learning?</b><br>
                        Cooperative learning is an instructional method in which students work together in small groups to achieve a common goal. This approach fosters peer interaction, where students learn by discussing, sharing ideas, and solving problems collaboratively. By working together, students not only enhance their own understanding but also help others develop their knowledge and skills.
                        
                    </p>
                    <p class="details" style="margin-top: 50px; width: 61%;"><b>A Look at Cooperative Learning:</b></p>
                    <ul class="details">
                        <li><b>Promotes Teamwork: </b> LiStudents collaborate, sharing responsibilities to achieve a common goal.</li>
                        <li><b>Encourages Communication: </b> Collaborative tasks help students improve their communication and interpersonal skills.</li>
                        <li><b>Develops Critical Thinkingt: </b> Working together on complex problems promotes analytical thinking and creativity.</li>
                        <li><b>Fosters Responsibility: </b> Each student has a role and is responsible for contributing to the group's success.</li>
                        <li><b>Builds Peer Relationships: </b>  Students learn from each other, gaining insights from their peers' strengths and ideas.</li>
                    </ul>
                    <p class="details" style="margin-top: 50px;"><b>How Cooperative Learning Can Be Implemented?</b></p>
                    <ol class="details">
                        <li>Group Projects</li>
                        <ul class="details">
                            <li>Students work together on a project, dividing tasks and sharing information to create a final product. </li>
                            <li>In a history class, students can research different aspects of a historical event and then present their findings to the class.</li>
                            <li> Encourages teamwork and ensures all students contribute to the project.</li>
                        </ul>
                        <br>
                        <li>Peer Tutoring</li>
                        <ul class="details">
                            <li>Students pair up and help each other understand challenging concepts.  </li>
                            <li>A student explains a difficult math problem to a peer, reinforcing both their own and the other student's understanding.</li>
                            <li>Reinforces learning through teaching, benefiting both tutor and tutee.</li>
                        </ul>
                        <br>
                        <li>Collaborative Problem-Solving</li>
                        <ul class="details">
                            <li>Groups of students work together to solve complex problems</li>
                            <li>In a science class, students collaborate to design an experiment or solve a practical problem.</li>
                            <li> Fosters critical thinking and teamwork in solving real-world problems.</li>
                        </ul>
                        <br>
                        <li>Jigsaw Method</li>
                        <ul class="details">
                            <li>Students are divided into small groups, each responsible for mastering a portion of the material. They then regroup to share what they’ve learned, piecing the information together.</li>
                            <li>In an English class, each group reads a section of a novel and then shares key points with others.</li>
                            <li> Encourages responsibility and helps students learn from each other’s knowledge.</li>
                        </ul>
                        <br>
                        <li>Collaborative Research</li>
                        <ul class="details">
                            <li>Students work in groups to research a topic and present their findings.</li>
                            <li> In a social studies class, students research a global issue like climate change and compile their results into a presentation.</li>
                            <li>Builds research skills while promoting teamwork.</li>
                        </ul>
                        <br>
                    </ol>  
